package com.bms.model;

public class LogStatus {

	private String tcuStatus;
	private String bmStatus;
	private String cfgStatus;

	
	public String getTcuStatus() {
		return tcuStatus;
	}
	public void setTcuStatus(String tcuStatus) {
		this.tcuStatus = tcuStatus;
	}

	public String getBmStatus() {
		return bmStatus;
	}
	public void setBmStatus(String bmStatus) {
		this.bmStatus = bmStatus;
	}

	public String getCfgStatus() {
		return cfgStatus;
	}
	public void setCfgStatus(String cfgStatus) {
		this.cfgStatus = cfgStatus;
	}
	
	
	
}
