package com.bms.model;

public class Version {

	String tcu;
	String bms;
	String cfg;
	
	String tcuVersion;
	String bmsVersion;
	String cfgVersion;
	public String getTcu() {
		return tcu;
	}
	public void setTcu(String tcu) {
		this.tcu = tcu;
	}
	public String getBms() {
		return bms;
	}
	public void setBms(String bms) {
		this.bms = bms;
	}
	public String getCfg() {
		return cfg;
	}
	public void setCfg(String cfg) {
		this.cfg = cfg;
	}
	public String getTcuVersion() {
		return tcuVersion;
	}
	public void setTcuVersion(String tcuVersion) {
		this.tcuVersion = tcuVersion;
	}
	public String getBmsVersion() {
		return bmsVersion;
	}
	public void setBmsVersion(String bmsVersion) {
		this.bmsVersion = bmsVersion;
	}
	public String getCfgVersion() {
		return cfgVersion;
	}
	public void setCfgVersion(String cfgVersion) {
		this.cfgVersion = cfgVersion;
	}
	
	
}
